package com.hanjullo.hanjulloapp;

public class rankingData {


    private String ranking;
    private String ranking_name;
    private String ranking_point;

    public rankingData() {
        this.ranking = ranking;
        this.ranking_name = ranking_name;
        this.ranking_point = ranking_point;
    }

    public String getRanking() {
        return ranking;
    }

    public void setRanking(String ranking) {
        this.ranking = ranking;
    }

    public String getRanking_name() {
        return ranking_name;
    }

    public void setRanking_name(String ranking_name) {
        this.ranking_name = ranking_name;
    }

    public String getRanking_point() {
        return ranking_point;
    }

    public void setRanking_point(String ranking_point) {
        this.ranking_point = ranking_point;
    }
}
